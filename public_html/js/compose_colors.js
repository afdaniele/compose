window.compose.palette = [
    "blue",
    "green",
    "red",
    "yellow",
    "purple",
    "cyan",
    "teal",
    "orange",
    "brown",
    "pink",
    "lime",
    "indigo"
];

class ColorPalette{
    constructor() {
        this.i = 0;
    }

    current(tone = 'standard', level = 1){
        let color = window.compose.palette[this.i];
        return ColorPalette.get(color, tone, level);
    }

    next(tone = 'standard', level = 1){
        let color = this.current(tone, level);
        this.i = (this.i + 1) % window.compose.palette.length;
        return color;
    }

    static get(color, tone = 'standard', level = 1) {
        let color_key = "{0}-{1}-{2}".format(color, tone, level);
        return window.compose.colors[color_key];
    }

    static geti(i, tone = 'standard', level = 1) {
        let idx = i % window.compose.palette.length;
        return ColorPalette.get(window.compose.palette[idx], tone, level);
    }
}